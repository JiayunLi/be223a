public class GateLocation {
    private String gatePlugins;
    private String gateSite;
    private String gateHome;

    public String getGatePlugins() {
        return gatePlugins;
    }

    public void setGatePlugins(String gatePlugins) {
        this.gatePlugins = gatePlugins;
    }

    public String getGateSite() {
        return gateSite;
    }

    public void setGateSite(String gateSite) {
        this.gateSite = gateSite;
    }

    public String getGateHome() {
        return gateHome;
    }

    public void setGateHome(String gateHome) {
        this.gateHome = gateHome;
    }
}
